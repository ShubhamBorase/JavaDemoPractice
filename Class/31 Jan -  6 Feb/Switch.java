

class Switch{
	public static void main(String[] args){

		int x = 15;
		switch (x){
			case 11:
				System.out.println("This is number 11");
				break;
			case 13:
                                System.out.println("This is number 13");
                                break;
			case 15:
                                System.out.println("This is number 15");
                                break;
			case 17:
                                System.out.println("This is number 17");
                                break;
		}
		
		System.out.println("We are out of the Switch statement");
	}
}
